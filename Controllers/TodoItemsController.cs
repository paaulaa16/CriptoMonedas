        private readonly TodoContext _context;

        public TodoItemsController(TodoContext context)
        {
            _context = context;
        }

        // GET: api/TodoItems
        [HttpGet]
        public async Task<ActionResult<IEnumerable<TodoItem>>> GetTodoItems()
        {
            var randomNumber = new Random();
           foreach (var criptoMoneda in _context.TodoItems) {
            // Añadir un valor random al valor Ultimo
                criptoMoneda.Ultimo = (decimal)randomNumber.NextDouble() * 100;
                // Actualizar el valor Maximo, si Ultimo > Maximo
                if (criptoMoneda.Max < criptoMoneda.Ultimo)
                {
                    criptoMoneda.Max = criptoMoneda.Ultimo;
                }
           }
           
                _context.SaveChanges();
                return await _context.TodoItems.ToListAsync();

        }

        // GET: api/TodoItems/5
        [HttpGet("{id}")]
        public async Task<ActionResult<TodoItem>> GetTodoItem(long id)
        {
            var todoItem = await _context.TodoItems.FindAsync(id);

            if (todoItem == null)
            {
                return NotFound();
            }

            var ran = new Random();

            // Añadir un valor random al valor Ultimo
            todoItem.Ultimo = (decimal)ran.NextDouble() * 100;
            // Actualizar el valor Maximo, si Ultimo > Maximo
            if (todoItem.Max < todoItem.Ultimo) {
                
                todoItem.Max = todoItem.Ultimo;
            }

            _context.SaveChanges();

            return todoItem;
        }

